var iteration = 11;

console.log('While');

while (iteration <= 10) {
    console.log('Iteratia nr ', iteration);
    iteration = iteration + 1;
}

console.log(iteration);

console.log('Do While');

iteration = 11;

do {
    console.log('Iteratia nr ', iteration);
    iteration = iteration + 1;
} while (iteration <= 10);

console.log(iteration);
